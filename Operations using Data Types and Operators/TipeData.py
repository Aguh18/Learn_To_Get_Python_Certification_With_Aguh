# Tipe data boolean
my_bool = True

# Tipe data integer
my_int = 10

# Tipe data float
my_float = 3.14

# Tipe data string
my_string = "Hello, World!"

print("tipe data dari variable my_bool adalah ",type(my_bool),"dengan nilai ", my_bool )
print("tipe data dari variable my_int adalah ",type(my_int),"dengan nilai ", my_int )
print("tipe data dari variable my_float adalah ",type(my_float),"dengan nilai ", my_float )
print("tipe data dari variable my_string adalah ",type(my_string),"dengan nilai ", my_string )