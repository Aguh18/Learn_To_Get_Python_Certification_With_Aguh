# konversi tipe data 
# konversi int ke float
x = 5
y = float(x)
print(y)
print(type(y))

# konversi float ke integer
x = 5.7
y = int(x)
print(y)
print(type(y))

#konversi String ke Int
x = "5"
y = int(x)
print(y)
print(type(y))

# Konversi String ke float
x = "5.7"
y = float(x)
print(y)
print(type(y))

#konversi Boolean ke Integer
x = True #nilai akan berubah menjadi 1
y = int(x)
print(y)
print(type(y))

# konversi float atau int ke string
x = 10 
y = str(x)
print(y)
print(type(y))

#konversi bool ke string
x = True
y = str(x)
print(y)
print(type(y))